// Primitives -  nn bb SS U
// NULL
let a = null;
// NUMBER
let b = 23;
// SYMBOL
let c = Symbol("this is a symbol");
// STRING
let d = "suraj";
// BOOLEAN
let e = true;
// BIGINT
let f = BigInt("56789");
// UNDEFINED
let g = undefined;

console.log(a, b, c, d, e, f, g);

//TYPEOF
console.log(typeof d);

